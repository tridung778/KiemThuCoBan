    boolean ValiDate() {
        Matcher matcherMaNH = Pattern.compile(MaNH_Pattern).matcher(txtMaNH.getText());
        if (!matcherMaNH.matches()) {
            DialogHelper.alert(this, "Mã người học phải đúng 7 ký tự");
            return false;
        }

        Matcher matcherHoTen = Pattern.compile(HoTen_Pattern).matcher(txtHoTen.getText());
        if (!matcherHoTen.matches()) {
            DialogHelper.alert(this, "Họ tên chỉ được chỉ chứa alphabet và ký tự trắng ");
            return false;
        }
        if (!isValidNgaySinh(txtNgaySinh.getText())) {
            DialogHelper.alert(this, "Ngày sinh không hợp lệ");
            return false;
        }

        if (!isValidEmail(txtEmail.getText())) {
            DialogHelper.alert(this, "Địa chỉ email không hợp lệ");
            return false;
        }

        return true;
    }

// Kiểm tra Ngày sinh (Trước ngày hiện tại ít nhất 16 năm)
    private boolean isValidNgaySinh(String ngaySinh) {
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            Date date = sdf.parse(ngaySinh);
            Calendar calendar = Calendar.getInstance();
            calendar.add(Calendar.YEAR, -16);
            return date.before(calendar.getTime());
        } catch (Exception e) {
            return false;
        }
    }

// Kiểm tra Địa chỉ email (Phải đúng định dạng email)
    private boolean isValidEmail(String email) {
        String regex = "^[A-Za-z0-9+_.-]+@(.+)$";
        Pattern pattern = Pattern.compile(regex);
        Matcher matcher = pattern.matcher(email);
        return matcher.matches();
    }

    void printExcel(JTable tbl) {
        String filePath = null;
        JFileChooser fileChooser = new JFileChooser();
        int returnValue = fileChooser.showSaveDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            filePath = selectedFile.getAbsolutePath();
        }

        filePath = filePath + ".xls";
        WritableWorkbook workbook = null;

        try {
            workbook = Workbook.createWorkbook(new File(filePath));
            WritableSheet sheet = workbook.createSheet("Dữ liệu từ JTable", 0);

            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            int rowCount = model.getRowCount();
            int columnCount = model.getColumnCount();

            // Đầu tiên, đưa tên cột vào dòng đầu tiên của tệp Excel
            for (int colIdx = 0; colIdx < columnCount; colIdx++) {
                Label label = new Label(colIdx, 0, model.getColumnName(colIdx)); // Lấy tên cột từ JTable
                sheet.addCell(label);
            }

            // Duyệt qua JTable và đổ dữ liệu vào tệp Excel từ dòng thứ hai
            for (int rowIdx = 0; rowIdx < rowCount; rowIdx++) {
                for (int colIdx = 0; colIdx < columnCount; colIdx++) {
                    Label label = new Label(colIdx, rowIdx + 1, model.getValueAt(rowIdx, colIdx).toString()); // Dòng thứ hai trở đi
                    sheet.addCell(label);
                }
            }

            workbook.write();
            workbook.close();

            // Hiển thị hộp thoại thông báo để hỏi người dùng có muốn mở tệp hay không
            int option = JOptionPane.showConfirmDialog(null, "Tệp đã được lưu thành công. Bạn có muốn mở tệp không?", "Thông báo", JOptionPane.YES_NO_OPTION);
            if (option == JOptionPane.YES_OPTION) {
                Desktop.getDesktop().open(new File(filePath)); // Mở tệp nếu người dùng chọn "Có"
            }
            logger.info("Người dùng in file exel thành công");
        } catch (Exception e) {
            DialogHelper.alert(this, "Không thể in file Excel!");
            logger.error(e);
        }
    }

